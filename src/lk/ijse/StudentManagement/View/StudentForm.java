    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            StudentDTO studentDTO=new StudentDTO();
            studentDTO.setStudentID(txtsID.getText());
            studentDTO.setStudentNAme(txtxsNAme.getText());
            studentDTO.setAddress(txtAddress.getText());
            studentDTO.setBirtday(txtBirthday.getText());
            studentDTO.setCourse(txtCourse.getText());
            studentDTO.setContact(txtxsContact.getText());
            System.out.println(studentDTO.getAddress());
            boolean result=studentController.saveStudent(studentDTO);
            if(result){
                JOptionPane.showMessageDialog(this, "Sucsess");
            }else{
                JOptionPane.showMessageDialog(this, "Fail");
            }
        } catch (Exception ex) {
            Logger.getLogger(StudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

