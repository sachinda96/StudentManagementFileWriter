public void loadAllStudents(){
     try {
            DefaultTableModel dtm = (DefaultTableModel) tblStudent.getModel();
            dtm.setRowCount(0);
        List<StudentDTO> allStudents;
        
            allStudents = studentController.getAllStudents();
        
            
                for (StudentDTO studentDto :allStudents) {
                    Object[] o = {studentDto.getStudentID(), studentDto.getStudentNAme(), studentDto.getAddress(),studentDto.getContact(),studentDto.getBirtday()};
                    dtm.addRow(o);
                
            }
                } catch (IOException ex) {
            Logger.getLogger(StudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
     }
